export const dictionaries: Record<Locale, Dictionary> = { en: {}, kn, hi };

export function translate(locale: Locale, source: string): string {
  if (locale === "en") return source;
  const dictionary = dictionaries[locale];
  const exact = dictionary[source];
  if (exact) return exact;

  const required = source.match(/^(.*?)(\s+\*)$/);
  if (required) {
    const translated = dictionary[required[1]];
    if (translated) return `${translated}${required[2]}`;
  }

  const compoundParts = source.split(/(\s+[·—]\s+)/);
  if (compoundParts.length > 1) {
    let changed = false;
    const localized = compoundParts.map((part, index) => {
      if (index % 2 === 1) return part;
      const translated = dictionary[part] ?? part;
      if (translated !== part) changed = true;
      return translated;
    });
    if (changed) return localized.join("");
  }

  return source;
}
